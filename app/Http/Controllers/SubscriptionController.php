    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\JsonResponse
     */
    public function store(Request $request)
    {
        $validator = Validator::make($request->all(), [
            'title' => 'required',
            'amount' => 'required|numeric',
            'timeframe' => 'required|numeric',
        ]);

        if ($validator->fails())
        {
            return response()->json(['success' => false, 'message' => $validator->errors()->toArray()], 422);
        }

        $subscription = Subscription::create([
            'title' => $request->title,
            'amount' => $request->amount,
            'timeframe' => $request->timeframe
        ]);

        if ( $subscription == null )
        {
            return response()->json(['success' => false, 'message' => ['An error occured subscription not added']], 422);
        }

        return response()->json(['success' => true, 'message' => 'Subscription was added successfully'], 200);
    }

